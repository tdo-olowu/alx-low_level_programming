#include "ops.h"

/**
 * add - adds two numbers
 * @a: a
 * @b: b
 * Return: integer
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - the difference of two numbers
 * @a: a
 * @b: b
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - the product of two numbers
 * @a: a
 * @b: b
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - the quotient of two numbers
 * @a: a
 * @b: b
 * Return: integer part of a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - the remainder when a is divided by b
 * @a: a
 * @b: b
 * Return: an integer
 */
int mod(int a, int b)
{
	return (a % b);
}
